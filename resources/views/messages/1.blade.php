                            <li> <b>He is our intercessor:</b> An intercessor is one who interceeds on behalf of another especially by prayers. Romans 8:28.</li>
                            <li> <b>He is our advocate:</b> An advocate is one who publicly supports or recommends a cause or policy.</li>
                            <li> <b>He is our strenghtener:</b> A strenghtener is one that provides additional strength. His indwelling eliminates unnecessary fatigue and weakness</li>
                            <li> <b>He is a Standby:</b> Standby means in readiness for duty or deployment. </li>
                            <li> <b>He is our revealer:</b> A revealer is a person that tells you the future. He shows you, tells you and reminds you all you need to know.</li>
                        </ul>

