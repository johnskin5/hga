                        <p class="message-head">The Holyspirit: </p>
                        <p class="message-body">He is the most important. He might not be seen but he is a person and he is our greatest destiny helper as he operates in two dimensions/realms. He operates in the spiritual (heaven) and in the physical (on earth). According to Romans 8:26-27,</p>
                        <ul>
                            <li> the holyspirit as a helper knows God's will for you. Jeremiah 29:11, 1 John 5:14. </li>
                            <li> He also knows the script written prior to your birth. </li>
                            <li> He knows the right prayer request thus he interceeds on your behalf. John 14:16, John 16:12-13. </li>
                            <li> He dwells in heaven while on earth and therefore should be desired by every believer as a best friend. </li>
                        </ul>
                        
                        <p class="message-body">To go deeper into our study of the holyspirit as a Destiny helper. We'd understudy 8 capacities through which he functions. </p>
                        <ul>
                            <li> <b>He is a Comforter (John 14:16.):</b> A comforter is one that comforts and soothes you during worrisome times. In other words, he pampers you and gives you spiritual massage.</li>
